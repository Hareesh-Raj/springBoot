package com.example.demo;

/*
 * User class is used to store the user details and to perform operations.
 * */
public class User {
	int userID;
	String Username;

	public int getUserID() {
		return userID;
	}

	public void setUserID(int userID) {
		this.userID = userID;
	}

	public String getUsername() {
		return Username;
	}

	public void setUsername(String username) {
		Username = username;
	}

}
